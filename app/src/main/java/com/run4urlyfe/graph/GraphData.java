package com.run4urlyfe.graph;

public class GraphData {
    private final double x;
    private final double y;

    public GraphData(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }
}
